import Anthropic from '@anthropic-ai/sdk'

const anthropic = process.env.ANTHROPIC_API_KEY
  ? new Anthropic({ apiKey: process.env.ANTHROPIC_API_KEY })
  : null

// Генерация бизнес-идеи для раунда
export async function generateIdea(roundNumber: number, previousIdeas: string[]): Promise<string> {
  if (!anthropic) {
    // Моковые идеи для разработки без API-ключа
    const mockIdeas = [
      'ИИ-ассистент, который даёт правильную формулу в Excel/Google Sheets по описанию на естественном языке. Ты говоришь ему «собери на отдельном листе студентов, которые не ответили на вопрос 5» — получаешь выражение, подставляешь и радуешься. Собирается за вечер; без кода. Продаём по подписке $9/мес. Маркетологи, сейлзы, аналитики, студенты, преподаватели, бухгалтеры — используют сложные формулы.',
      'Телеграм-бот, который каждое утро присылает краткое содержание всех непрочитанных статей из Pocket/Instapaper. Пользователь скидывает ссылки в бота, а он с помощью GPT делает саммари на 3-4 предложения. Монетизация: freemium, до 5 статей бесплатно, потом $4.99/мес. Целевая аудитория — продакт-менеджеры и маркетологи, которые копят статьи.',
      'Платформа для автоматической генерации коммерческих предложений. Менеджер по продажам загружает бриф клиента, система генерирует красиво оформленное КП с расчётами, кейсами и презентацией. Интеграция с CRM. Подписка $49/мес за команду. Целевая: B2B-компании со штатом сейлзов от 5 человек.',
      'AI-платформа для автоматической оценки качества звонков в колл-центрах. Записи звонков транскрибируются и анализируются: вежливость, следование скрипту, выявление негатива. Дашборд для руководителя с метриками по каждому оператору. Подписка от $200/мес. Целевая: колл-центры от 20 операторов.',
      'Сервис AI-дизайна интерьеров. Пользователь фотографирует комнату, выбирает стиль — получает фотореалистичный рендер нового интерьера за 30 секунд. Рядом — список мебели с ценами и ссылками на покупку (партнёрка). Монетизация: $14.99/мес или оплата за рендер. Партнёрские комиссии от мебельных магазинов.',
    ]
    return mockIdeas[Math.min(roundNumber - 1, mockIdeas.length - 1)]
  }

  const systemPrompt = `Ты — генератор бизнес-идей для игры «Почему нет».
Сгенерируй одну бизнес-идею для AI-продукта.
ФОРМАТ: 3-5 предложений. Что делает продукт, как собирается, кому продаётся, модель монетизации.
СЛОЖНОСТЬ РАУНДОВ:
- Раунды 1-2: очевидно слабые идеи (обёртки для ChatGPT, саммаризаторы, агрегаторы без ценности)
- Раунд 3: средняя идея (есть зерно, но серьёзные проблемы)
- Раунды 4-5: идеи-ловушки (выглядят привлекательно, но имеют скрытые проблемы — огромный скоуп, высокий CAC, сложная монетизация, длинный цикл)
ПРЕДЫДУЩИЕ ИДЕИ (не повторять): ${previousIdeas.length > 0 ? previousIdeas.join(' | ') : 'нет'}
ТЕКУЩИЙ РАУНД: ${roundNumber}
Язык: русский, простой и понятный. Без канцелярита.
Не используй слова: «инновационный», «уникальный», «революционный», «синергия».
Верни ТОЛЬКО текст идеи, без заголовков и нумерации.`

  const response = await anthropic.messages.create({
    model: 'claude-sonnet-4-20250514',
    max_tokens: 500,
    messages: [{ role: 'user', content: 'Сгенерируй идею.' }],
    system: systemPrompt,
  })

  const content = response.content[0]
  return content.type === 'text' ? content.text : 'Ошибка генерации идеи'
}

// Оценка аргументов участника
export interface ArgumentEvaluation {
  text: string
  accepted: boolean
  feedback: string
  criterion: string
}

export interface EvaluationResult {
  hit: boolean
  arguments: ArgumentEvaluation[]
}

export async function evaluateArguments(
  ideaText: string,
  args: [string, string, string]
): Promise<EvaluationResult> {
  if (!anthropic) {
    // Моковая оценка для разработки
    const accepted = args.map(a => a.length > 20)
    const hitCount = accepted.filter(Boolean).length
    return {
      hit: hitCount >= 2,
      arguments: args.map((text, i) => ({
        text,
        accepted: accepted[i],
        feedback: accepted[i]
          ? ''
          : `Слишком общо. Конкретизируй: какой именно рынок? Какие цифры? Кто конкурент?`,
        criterion: accepted[i]
          ? `Критерий из аргумента: "${text.slice(0, 50)}"`
          : '',
      })),
    }
  }

  const systemPrompt = `Ты — оценщик аргументов в игре «Почему нет».
Участник играет роль венчурного инвестора и отказывает идее.

ИДЕЯ: ${ideaText}

АРГУМЕНТЫ УЧАСТНИКА:
1. ${args[0]}
2. ${args[1]}
3. ${args[2]}

ЗАДАЧА: Оцени каждый аргумент — ПРИНЯТ или ОТКЛОНЁН.

ПРИНЯТ = конкретная, измеримая причина отказа.
Содержит: конкретную метрику, название рынка, тип клиента, бизнес-модель, конкурента, техническое ограничение.
Примеры: «CAC выше LTV из-за платного трафика», «ChatGPT уже делает это бесплатно — нет защиты», «цикл B2B-продажи 3-6 месяцев, нужен отдел сейлзов»

ОТКЛОНЁН = общая фраза, которую нельзя превратить в измеримый критерий.
Примеры: «нет спроса», «слишком дорого», «никто не купит», «идея плохая», «много конкурентов»

ВАЖНО: Не придирайся. Если аргумент содержит хоть одну конкретную деталь — принимай. Отклоняй только суперобщие, ничего не значащие фразы.

ПОПАДАНИЕ = минимум 2 из 3 аргументов ПРИНЯТЫ.

Тон обратной связи при отклонении: честно, цинично, по существу, коротко. 1-2 предложения. Всегда покажи, как должен выглядеть хороший аргумент. Без канцелярита и AI-звучащих фраз.

Для принятых аргументов — «переверни» в позитивный критерий хорошего продукта.

Верни СТРОГО JSON (без markdown, без \`\`\`):
{
  "hit": true/false,
  "arguments": [
    {"text": "текст аргумента", "accepted": true/false, "feedback": "объяснение если отклонён, пустая строка если принят", "criterion": "критерий если принят, пустая строка если отклонён"}
  ]
}`

  const response = await anthropic.messages.create({
    model: 'claude-sonnet-4-20250514',
    max_tokens: 1000,
    messages: [{ role: 'user', content: 'Оцени аргументы.' }],
    system: systemPrompt,
  })

  const content = response.content[0]
  if (content.type !== 'text') {
    throw new Error('Ошибка оценки')
  }

  try {
    return JSON.parse(content.text) as EvaluationResult
  } catch {
    // Попробуем извлечь JSON из текста
    const match = content.text.match(/\{[\s\S]*\}/)
    if (match) {
      return JSON.parse(match[0]) as EvaluationResult
    }
    throw new Error('Не удалось распарсить ответ AI')
  }
}
